#include "libft.h"

void	ft_cutstr(char *s1, char *s2, char c)
{
	int	i;

	i = 0;
	while (s2[i] && s2[i] != c)
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = 0;
	i++;
	while (s2[i])
	{
		*s2 = s2[i];
		s2++;
	}
	*s2 = 0;
}
